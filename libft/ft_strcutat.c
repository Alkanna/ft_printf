#include "libft.h"

int		ft_strcutat(char **str, char find)
{
	int i;

	i = 0;
	while ((*str)[i])
	{
		if ((*str)[i] == find)
		{
			ft_strclr(*str + i);
			return (1);
		}
		i++;
	}
	return (0);
}
