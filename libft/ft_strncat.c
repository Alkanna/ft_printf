#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		len;
	size_t	j;

	j = 0;
	len = ft_strlen(s1);
	while (s2[j] && j < n)
	{
		s1[len + j] = s2[j];
		j++;
	}
	s1[len + j] = '\0';
	return (s1);
}
